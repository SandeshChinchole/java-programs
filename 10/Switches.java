
public class Switches {

	public static void main(String[] args) {
		
		String day = "Monday";
		
		switch(day) {
		
		case "Sunday": System.out.println("It's Sunday");
		break;
		
		case "Monday": System.out.println("It's Monday");
		break;
		
		default: System.out.println("It's not a day");
		
		}
	}
}
