
public class WrapperClasses {

	public static void main(String[] args) {
		
		//autoboxing 

		Boolean a = true;
		Character b = '@';
		Double c = 3.14;
		Integer d = 123;
		String e = "Neo";
		
		//unboxing: the reference data types behave similar to their primitive equivalent
		
		if(a == true) {
			System.out.println("This is true");
		}
	}

}
